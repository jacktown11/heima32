package day33;

public class Person6 {
	private String name;
	private int age;
	
	public Person6() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	@Override
	public String toString() {
		return "Person6 [name=" + name + ", age=" + age + "]";
	}
	
}
